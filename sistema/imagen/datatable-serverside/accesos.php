		if($acceso_user==1){
			$row[4] = "<div align='center'><button type='button' name='$row[3]' lang='$row[1]' class='btn btn-danger btn-xs' onClick='delete_a($row[5])'><i class='fa fa-trash' aria-hidden='true'></i></button></div>";
		}else{ $row[4] = "-"; }
		
